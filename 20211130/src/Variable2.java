
public class Variable2 {

	public static void main(String[] args) {
		// 변수를 선언하고 처음 값을 저장할 경우 이 값을 초기값이라 한다.
		// 처음 값을 지정하는 행위를 '초기화'라고 한다.
		
		// 변수의 선언
		int b, c; //동시에 2개 이상도 가능.
		b = 10; 
		c = 15;
		
		int a = 5; //선언과 동시에 값을 지정하기도 가능.
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		a = 20; // 선언한 값을 갱신도 가능.
		System.out.println(a);
	}
}

