
public class Variable {

	public static void main(String[] args) {
		
		// 변수는 일종의 박스이다.
		// [데이터 유형] [변수 식별자 이름];
		// 이 박스에는 넣을 물건의 종류와 박스명칭을 부여해야한다.
		// 존재하지 않는 변수는 호출이 안된다.
		
		// 아래는 "정수" 라는 물건을 a라는 이름의 박스에 담을 예정이라는 의미이다.
		int a;
		// a 박스에 5를 집어 넣습니다.
		a = 5;
		// a 박스에 들어있는 값을 콘솔에 찍어봅니다. (a에 따옴표가 안붙어있음에 주의)
		System.out.println(a);
		
		// 정수를 담을 수 있는 A 라는 박스를 생성해주세요.
		int A;
		// A박스에 10을 넣어주세요.
		A = 10;
		// A박스에 들어있는 값을 콘솔에 출력해보세요.
		System.out.println(A);
		
	}

}
