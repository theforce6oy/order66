
public class ifElse01_95p {
	public static void main(String[] args) {
		
		// if~else 구문은 참일때 실행할 구문과 거짓일떄 실행할 구문을 작성한다.
		// if else 구문은 한 구문으로 간주되기에 연산횟수를 절약한다.
		// else 구문은 if문 없이 단독으로 쓰일 수 없다.*
		// if문에만 조건식을 작성 해야한다. else는 작성할 필요 없음.*
		
		int a = 15;
		
		if( a == 15){
			System.out.println("15 입니다.");
			}
		else { // if(a != 15){}
			System.out.println("15가 아닙니다.");
		}	
	    }
        }

