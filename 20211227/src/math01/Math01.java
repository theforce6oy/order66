package math01;

public class Math01 {

	public static void main(String[] args) {
		
		//Math는 import없이 쓸 수 있다.
		
		int a = -1024;
		System.out.println(Math.abs(a));
		//절대값
		
		double b = 3.14;
		System.out.println(Math.ceil(b));
		//소수점 올림
		
		double c = 19.9;
		System.out.println(Math.floor(c));
		//소수점 내림
		
		
		
		

	}

}
