
public class for01_112p {

	public static void main(String[] args) {
		
		// for 구문은 정해진 조건만큼 반복문을 실행.
		// for(시작변수; 종료[조건]; 증감식) {
		//    실행문1;
		//    실행문2;.....}
		//로 이루어짐.
		
		// 조건이 false일시 멈춤.
		
		for(int a = 0; a < 2; a++) //(초기식(1), 반복범위를 체크하여 반복여부결정, 증감)
		{                          // (1,2,3)의 순서로 작용함.
		System.out.println("for 구문을 이용한 반복문");
		}
	}
}
