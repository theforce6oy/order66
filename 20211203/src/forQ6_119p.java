
public class forQ6_119p {

	public static void main(String[] args) {
		
		// 다중for문을 이용하여 피라미드별 만들기
		
		for(int a = 5; a>0; a--) {
			for(int b = 1; b<a; b++) {
				System.out.print("*");}
		System.out.println("*");}
		
		
		System.out.println("");
		for(int a = 4; a>0; a--) {
			for(int b = 0; b<a; b++) {
				System.out.print("*");}
		System.out.println("");}
		}

	}


